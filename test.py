# 1) Используя цикл while, сгенерировать список с диапазоном значений от 0 до 15. Вывести результат

# Создаем пустой список
numbers = []

# Устанавливаем начальное значение
i = 0

# Используем цикл while для добавления чисел от 0 до 15
while i <= 15:
    numbers.append(i)
    i += 1

# Выводим результат
print(f"Результат задания №1: {numbers}")

# 2) Используя цикл while, сгенерировать множество с диапазоном значений от 0 до 10. Вывести результат

# Создаем пустое множество
numbers_set = set()

# Устанавливаем начальное значение
i = 0

# Используем цикл while с break для добавления чисел от 0 до 10
while True:
    if i > 10:
        break
    numbers_set.add(i)
    i += 1

# Выводим результат
print(f"Результат задания №2: {numbers_set}")

# 3) Используя цикл while, удалить из списка все элементы, которые меньше 11. Вывести результат

print(f"Исходный список: {numbers}")

# Устанавливаем начальное значение
i = 0

# Используем цикл while для удаления элементов меньше 11
while i < len(numbers):
    if numbers[i] < 11:
        # Удаляем элемент по индексу
        numbers.pop(i)
        # Не увеличиваем i, так как после удаления элементы сдвигаются
    else:
        i += 1

# Выводим результат
print(f"Результат после удаления: {numbers}")