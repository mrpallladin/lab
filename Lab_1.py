#5)	Написать функцию, на вход которой подается два списка в одном значение длины, а другом ширины. Найти площади круга, 
#   описывающего каждый из прямоугольников и записать в кортеж. Найти среднее площадей и удалить все значения из списка больше неё на 10%. 
#   Из полученных значений найти произведение и поделить его на длину входного списка и вывести ответ. 
#   Итоговый ответ вернуть и вывести на экран. 
#   При выполнении задания использовать функцию filter().

#Определяем функцию

def circle_areas(length, width):

    #1 Находем площади описанных окружностей
    areas = []
    for l, w in zip(length, width):
        diagonal = l**2 + w**2
        circle_areas = math.pi * diagonal / 4
        areas.append(circle_areas)

    areas_tuple = tuple(areas)
    print(f"Площади описанных окружностей: {areas}")

    #2 Найдем среднее площадей
    mid_areas = sum(areas_tuple) / len(areas_tuple)
    print(f"Среднее значение площадей: {mid_areas}")

    #3 Удалим площади, которые на 10% больше средней
    threshold = mid_areas * 1.1
    filtered_areas = list(filter(lambda x: x <= threshold, areas_tuple))
    print("После фильтрации:", filtered_areas)

    #4 найдем произведение оставшихся площадей
    res = math.prod(filtered_areas)
    #print(res)

    #5 Делим на длину длину входного списка
    result = res / len(length)
    #print(result)

    #6 Возвращаем результат работы функции
    return result

