# 6) Написать функцию, на вход которой подается наименьшее, наибольшее значение и шаг. 
#    По этим параметрам сгенерировать заполненный список. 
#    Посчитать количество чисел кратных 7 и отнять от него медиану всех чисел списка. 
#    Если полученный результат отрицательный необходимо зеркально отобразить список, если положительный, 
#         то создать копию старого списка и добавить в его начало полученное число. 
#    Итоговый список вернуть и вывести на экран. 
#    При выполнении задания использовать функцию filter().

def create_list(min_val, max_val, step):

    #1 Генерируем список по полученным условиям
    res_list = []
    corrent = min_val
    
    while corrent <= max_val:
        res_list.append(corrent)
        corrent += step
    print(f"Созданный список: {res_list}")

    #2 Посчитаем количество элементов кратных 7
    count = len(list(filter(lambda x: x % 7 == 0, res_list)))
    print(f"Количество элементов кратных 7: {count}")

    #3 находим медиану
    sorted_list = sorted(res_list)
    n = len(sorted_list)

    if n % 2 == 1:
        median = sorted_list[n // 2]
    else:
        median = (sorted_list[n // 2 - 1] + sorted_list[n // 2]) / 2
    print(f"Медиана списка: {median}")

    #4 число элементов кратных 7 - медиана
    difference = count - median

    #5 Обработаем результат в зависимости от знака
    if difference < 0:
        # Зеркально отображаем список
        final_list = res_list[::-1]
        print("Результат отрицательный, список отображен зеркально")
    else:
        # Создаем копию и добавляем результат в начало
        final_list = res_list
        final_list.insert(0, difference)
        print("Результат положительный, элемент добавлен в начало списка")

    #6 Возвращаем результат функции
    return final_list

